package Ejercicio3_6;

public class Estudiante {
    String nombre;
    String apellido;
    int codigo;
    int numeroSemestre;
    float notaFinal;

    public Estudiante(String nombre, String apellido, int codigo, int numeroSemestre, float notaFinal) {
        this.nombre = nombre;
        this.apellido = apellido;
        this.codigo = codigo;
        this.numeroSemestre = numeroSemestre;
        this.notaFinal = notaFinal;
    }
}
